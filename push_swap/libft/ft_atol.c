#include "libft.h"

long int	ft_atol(const char *nptr)
{
	long int	result;
	int			signe;

	signe = 1;
	result = 0;
	while ((*nptr >= 9 && *nptr <= 13) || *nptr == 32)
		nptr++;
	if (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			signe = -1;
		nptr++;
	}
	while (*nptr && ft_isdigit(*nptr))
		result = result * 10 + (*nptr++ - 48);
	return (result * signe);
}
